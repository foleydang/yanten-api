def load_jokes():
    try:
        with open(JOKES_FILE, 'r', encoding='utf-8') as f:
            return json.load(f)
    except:
        return []

def save_jokes(jokes):
    backup_dir = '/root/logs/jokes-backup'
    os.makedirs(backup_dir, exist_ok=True)
    backup_file = f"{backup_dir}/jokes_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
    with open(backup_file, 'w', encoding='utf-8') as f:
        json.dump(jokes, f, ensure_ascii=False, indent=2)
    with open(JOKES_FILE, 'w', encoding='utf-8') as f:
        json.dump(jokes, f, ensure_ascii=False, indent=2)

def main():
    print('=' * 60)
    print(f'哇哇笑笑话更新 - {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}')
    print('=' * 60)
    
    jokes = load_jokes()
    existing_titles = set(j['title'] for j in jokes)
    print(f'当前笑话数: {len(jokes)}')
    print(f'本地笑话库: {len(LOCAL_JOKES_DB)} 条')
    print(f'每日目标: {DAILY_LIMIT}')
    
    # 从本地库中选择未添加的笑话
    available = [j for j in LOCAL_JOKES_DB if j['title'] not in existing_titles]
    print(f'可添加笑话: {len(available)} 条')
    
    if not available:
        print('❌ 本地笑话库已用完，需要补充更多笑话')
        return
    
    # 随机选择
    selected = random.sample(available, min(DAILY_LIMIT, len(available)))
    
    # 分配ID
    max_id = max([j['id'] for j in jokes]) if jokes else 0
    today = date.today().strftime('%Y-%m-%d')
    
    for j in selected:
        max_id += 1
        j['id'] = max_id
        j['likes'] = 0
        j['neutrals'] = 0
        j['dislikes'] = 0
        j['shares'] = 0
        j['isHot'] = False
        j['status'] = 'approved'
        j['createdAt'] = int(datetime.now().timestamp() * 1000)
        j['date'] = today
    
    jokes.extend(selected)
    save_jokes(jokes)
    
    print(f'\n✅ 更新完成!')
    print(f'新增: {len(selected)} 条')
    
    # 来源统计
    sources = {}
    for j in selected:
        sources[j['category']] = sources.get(j['category'], 0) + 1
    for s, c in sources.items():
        print(f'  {s}: {c} 条')
    
    print(f'总数: {len(jokes)} 条')
    print(f'本地库剩余: {len(LOCAL_JOKES_DB) - len(set(j["title"] for j in jokes))} 条')
    
    print('\n预览:')
    for j in selected[:5]:
        print(f'  [{j["category"]}] {j["title"]}')
        print(f'    {j["content"][:60]}...')

if __name__ == '__main__':
    main()
